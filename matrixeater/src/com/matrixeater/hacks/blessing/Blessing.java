package com.matrixeater.hacks.blessing;

public class Blessing {
	private final String loaName;
	private final String loaSubtitle;

	public Blessing(final String loaName, final String loaSubtitle) {
		super();
		this.loaName = loaName;
		this.loaSubtitle = loaSubtitle;
	}

	public String getLoaName() {
		return loaName;
	}

	public String getLoaSubtitle() {
		return loaSubtitle;
	}

}
